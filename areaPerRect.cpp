// This program calculates the area and perimeter of a rectangle.
//    with dimensions of 7cm x 9cm

#include<iostream>

int main() {
    std::cout <<"If a rectangle has the dimensions:" << std::endl;
    std::cout << "7cm x 9cm" << std::endl;
    std::cout << "" << std::endl;
    std::cout << "Area is: " << (7 * 9) << "cm^2" << std::endl;
    std::cout << "Perimeter is:" << (2* (7+9)) << "cm" << std::endl;
}
